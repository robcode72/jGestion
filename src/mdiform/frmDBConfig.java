    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String db_url = "jdbc:oracle:thin:@" + txtServer.getText()+ ":" + txtPort.getText() +
                                                   ":" + txtService.getText(); 
            Connection conn = DriverManager.getConnection(  
                    db_url,txtUser.getText(),txtPassword.getText());
            Statement stmt = conn.createStatement();
            
            JOptionPane.showMessageDialog(this, "Se ha establecidad correctamente la conexi√≥n con " + 
                                       txtServer.getText()+ ":" + txtPort.getText());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

