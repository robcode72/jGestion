    private void jbttnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbttnAceptarActionPerformed
        // TODO add your handling code here:
        
        Utiles utiles = new Utiles();
        String[] NewConfig = new String[3];
        NewConfig[0] = txtServer.getText();
        NewConfig[1] = txtPort.getText();
        NewConfig[2] = txtService.getText();
        
        try {
            try {
                
                if (JOptionPane.showConfirmDialog(this, "¬øEstas seguro que deseas cambiar la configuraci√≥n?", "Guardar los datos", 0,0) == 0){
                    utiles.WriteXMLConfig(NewConfig);
                    dbconfig[0] = NewConfig[0];
                    dbconfig[1] = NewConfig[1];
                    dbconfig[2] = NewConfig[2];
                    NewJFrame.dbURL = "jdbc:oracle:thin:@" + dbconfig[0]+ ":" + dbconfig[1] +
                                                   ":" + dbconfig[2];
                }
                
            } catch (TransformerException ex) {
                Logger.getLogger(frmDBConfig.class.getName()).log(Level.SEVERE, null, ex);
            } catch (XPathExpressionException ex) {
                Logger.getLogger(frmDBConfig.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ParserConfigurationException ex) {
            Logger.getLogger(frmDBConfig.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SAXException ex) {
            Logger.getLogger(frmDBConfig.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(frmDBConfig.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbttnAceptarActionPerformed

