    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Guardar Cambios IVAS
        
        // Comprobar Valores
        if (edtNomIVA.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debes de introducir una descripción", "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
            edtNomIVA.setFocusable(true);
            return;
        }

        if (edtIVA.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debes de introducir el IVA", "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
            edtIVA.setFocusable(true);
            return;
        }
        
        String sIVA = edtIVA.getText().toString();
        Integer IVA = Integer.valueOf(sIVA);
        String NomIVA = edtNomIVA.getText().toString();
        
        Guardar_IVA(NomIVA, IVA);
        dispose();
        
    }//GEN-LAST:event_jButton1ActionPerformed

