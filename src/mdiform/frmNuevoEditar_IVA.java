    private void Guardar_IVA(String NomIVA, Integer Iva) {
       // Muestra la información del cliente para poder editarlo
        try {
            // Send Query
            String Quotes = "\"";

            Connection connection = null;
            Statement stmt = null;
            CallableStatement cs = null;
            ResultSet rs = null;

            // Comprobar el Estado 0=Editar 1=Nuevo
            String SQL;
            if (Estado == 1){
                SQL = "{call XGESTION_PKG.INSERTAR_IVA(?, ?)}";
            }else{
                SQL = "{call XGESTION_PKG.ACTUALIZAR_IVA(?, ?, ?)}";
            }

            connection = DriverManager.getConnection(NewJFrame.dbURL, NewJFrame.username, NewJFrame.password);
            stmt = connection.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,
                    ResultSet.CONCUR_UPDATABLE);
            cs = connection.prepareCall(SQL);        
            
            
            /***XGESTION_PKG.INSERTAR_IVA
             * p_nomiva   IN IVAS.NOMIVA%TYPE,
               p_iva      IN IVAS.IVA%TYPE
             */
            
            if (Estado == 1){
                cs.setString(1, NomIVA);
                cs.setInt(2, Iva);
            }
            else{
                
            /***XGESTION_PKG.ACTUALIZAR_IVA
             *p_codiva   IN IVAS.CODIVA%TYPE,
              p_nomiva   IN IVAS.NOMIVA%TYPE,
              p_iva      IN IVAS.IVA%TYPE)
              */
            
                cs.setInt(1, CodIVA);    
                cs.setString(2, NomIVA);
                cs.setInt(3, Iva);    
                
            }
            
            cs.execute();

            cs.close();
           
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage().toString());
        }
    }

